    function detectLanguage() {
        const preferred = Array.isArray(navigator.languages) && navigator.languages.length
            ? navigator.languages
            : [navigator.language || navigator.userLanguage || 'en'];

        const normalized = preferred
            .map(l => (l || '').toLowerCase())
            .map(l => l.split('-')[0])
            .filter(Boolean);

        const supported = Object.keys(translations);
        const match = normalized.find(l => supported.includes(l));
        return match || 'en';
    }

    const userLang = detectLanguage();
    const lang = translations[userLang] || translations.en;

    function applyTranslations() {
        if (['ar', 'ur', 'fa'].includes(userLang)) {
            document.documentElement.setAttribute('dir', 'rtl');
            document.body.setAttribute('dir', 'rtl');
        }

        document.documentElement.lang = userLang;

        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (lang[key]) {
                element.textContent = lang[key];
            }
        });

        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (lang[key]) {
                element.placeholder = lang[key];
            }
        });
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', applyTranslations);
    } else {
        applyTranslations();
    }
